// Tipagem Dinâmica

// Existe uma função dentro do JavaScript chamada TYPEOFF, que exibe o tipo das variáveis.

let nome = "Caio";
console.log(typeof nome); //string

// Por ser linguagem dinâmica, Javascript permite alterações do tipo de valor da variável com facilidade.
nome = 5;
console.log(typeof nome); // number

let estaAprovado = true;
console.log(estaAprovado); // true - Tipo booleano.

// Quando se atribui "undefined" para uma variável, você está passando um valor para ela, o que não é recomendado.
