// Operadores de igualdade
// Igualdade estrita: (===)
// Igualdade estrita analisa valor e tipo

console.log(1 === 1); // True
console.log(1 === '1'); // False

// Igualdade "Solta" ( == )
// Ignora tipo, sรณ analisa valor, caso seja de tipo diferente, vai converter.

console.log(2 == 2); //True
console.log(2 == '2'); //True